let somarDoisNumeros = function somarDoisNumeros(numA, numB) {
    return numA + numB;
}

let subtrairDoisNumeros = (numA, numB) => {
    return numA - numB;
}

let multiplicarDoisNumeros = (numA, numB) => {
    return numA * numB;
}

let dividirDoisNumeros = (numA, numB) => numA / numB;

module.exports = {
    somarDoisNumeros,
    subtrairDoisNumeros,
    multiplicarDoisNumeros,
    dividirDoisNumeros
};
